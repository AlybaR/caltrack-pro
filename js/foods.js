/* ---------- Local search function ---------- */
function searchLocalDB(query) {
    if (!query || query.length < 2) return [];
    const terms = query.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '').split(/\s+/).filter(t => t.length > 1);
    const normalize = s => s.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');

    return FOODS_DB
        .map(f => {
            const name = normalize(f.n);
            let score = 0;
            if (terms.every(t => name.includes(t))) score += 3;
            else if (terms.some(t => name.includes(t))) score += 1;
            else return null;
            if (name.startsWith(normalize(terms[0]))) score += 2;
            return { ...f, _score: score };
        })
        .filter(Boolean)
        .sort((a, b) => b._score - a._score)
        .slice(0, 12)
        .map(({ _score, ...f }) => f);
}
